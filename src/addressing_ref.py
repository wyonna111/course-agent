REF_8086_ADDRESSING = """
【8086 寻址判题参考 — 分析时对照，仍以本页表格与课件表述为准】
- 立即数：操作数在指令中；**不访问内存，无物理地址**。
- 寄存器：操作数在寄存器中；**不访问内存，无物理地址**。
- 直接寻址：有效地址=指令中的地址/符号；物理地址=**段基址×16+偏移**（默认 DS，如未写段）。
- 寄存器间接：[BX]/[BP]/[SI]/[DI]；物理地址=**段×16+寄存器内容**（[BP] 默认 SS，其余默认 DS）。
- 寄存器相对：[BX+disp]、[SI+disp] 等；物理地址=段×16+(寄存器+disp)。
- 基址变址：[BX+SI+disp] 等；物理地址=段×16+(BX+SI+disp)。
- 段超越 ES:[BX]：物理地址=**ES×16+BX**。
- 判错：课件「寻址方式」名称明显不对（如 [BX] 写成寄存器）；或「物理地址」把立即数/寄存器值当内存地址；或直接寻址只写偏移未加段基址。
"""


def is_addressing_table_page(text: str) -> bool:
    u = text.upper()
    return u.count("MOV") >= 3 and any(k in text for k in ("寻址", "物理地址", "DS=", "选项"))
